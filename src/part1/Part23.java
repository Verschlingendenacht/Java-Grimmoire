public class Part23 {
	static void master() {
		
		//final -> palabra clave que hace que no podamos cambiar el valor de la variable en cualquier parte del codigo y su ejecucion misma
		
		final double PI = 3.14159;
		
		//pi = 3.145; //esto resulta en error
		
		System.out.println(PI);
		
		//por convencion, las variables declaradas como final son escritas todo en mayusculas
	}
}
